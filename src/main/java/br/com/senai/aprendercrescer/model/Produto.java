
package br.com.senai.aprendercrescer.model;

import br.com.senai.aprendercrescer.model.*;


public class Produto {

    int idProduto ;
    String precoNormal;
    String tipoProduto ;
    String flagInativo;
    String dtAlteracao;
    String dataCadastro;
   

    public int getIdProduto() {
        return idProduto;
    }

    public void setIdProduto(int idProduto) {
        this.idProduto = idProduto;
    }

    public String getPrecoNormal() {
        return precoNormal;
    }

    public void setPrecoNormal(String precoNormal) {
        this.precoNormal = precoNormal;
    }

    public String getTipoProduto() {
        return tipoProduto;
    }

    public void setTipoProduto(String tipoProduto) {
        this.tipoProduto = tipoProduto;
    }

    public String getFlagInativo() {
        return flagInativo;
    }

    public void setFlagInativo(String flagInativo) {
        this.flagInativo = flagInativo;
    }

    public String getDtAlteracao() {
        return dtAlteracao;
    }

    public void setDtAlteracao(String dtAlteracao) {
        this.dtAlteracao = dtAlteracao;
    }

    public String getDataCadastro() {
        return dataCadastro;
    }

    public void setDataCadastro(String dataCadastro) {
        this.dataCadastro = dataCadastro;
    }
   
  
}
   


									
								